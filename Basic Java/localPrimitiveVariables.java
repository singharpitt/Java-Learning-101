public class localPrimitiveVariables {
    public static void main(String[] args) {
        // Local primitive variables
        int localInt = 42;                 // Integer variable
        double localDouble = 3.14;         // Double-precision floating-point variable
        boolean localBoolean = true;       // Boolean variable
        char localChar = 'A';              // Character variable
        byte localByte = 10;               // Byte variable
        short localShort = 2000;           // Short integer variable
        long localLong = 100000L;          // Long integer variable
        float localFloat = 5.75f;          // Single-precision floating-point variable

        // Print local variables
        System.out.println("Local Variables:");
        System.out.println("localInt: " + localInt);
        System.out.println("localDouble: " + localDouble);
        System.out.println("localBoolean: " + localBoolean);
        System.out.println("localChar: " + localChar);
        System.out.println("localByte: " + localByte);
        System.out.println("localShort: " + localShort);
        System.out.println("localLong: " + localLong);
        System.out.println("localFloat: " + localFloat);
    }
}
